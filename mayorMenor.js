function comparacionNumeros (numero1, numero2){
    if(numero1 > numero2){
        console.log(numero1 + " es mayor a " + numero2)
    }
    else if(numero1 < numero2){
        console.log(numero1 + " es menor a " + numero2)
    }
    if(numero1 == numero2){
        console.log(numero1 + " es igual a " + numero2)
    }
    
}